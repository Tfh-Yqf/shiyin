package com.example.shiyin.Suanfa;

public class Notedown {

        private float betime;
        private float entime;
        private int note;

    public float getBetime() {
        return betime;
    }

    public void setBetime(float betime) {
        this.betime = betime;
    }

    public float getEntime() {
        return entime;
    }

    public void setEntime(float entime) {
        this.entime = entime;
    }

    public int getNote() {
        return note;
    }

    public void setNote(int note) {
        this.note = note;
    }
}
